using System;
using Microsoft.AspNetCore.Hosting;
using Microsoft.AspNetCore.Identity;
using Microsoft.AspNetCore.Identity.UI;
using Microsoft.EntityFrameworkCore;
using Microsoft.Extensions.Configuration;
using Microsoft.Extensions.DependencyInjection;
using Online_Ticket_Booking_System.Areas.Identity.Data;
using Online_Ticket_Booking_System.Data;

[assembly: HostingStartup(typeof(Online_Ticket_Booking_System.Areas.Identity.IdentityHostingStartup))]
namespace Online_Ticket_Booking_System.Areas.Identity
{
    public class IdentityHostingStartup : IHostingStartup
    {
        public void Configure(IWebHostBuilder builder)
        {
            builder.ConfigureServices((context, services) => {
                services.AddDbContext<AuthDbContext>(options =>
                    options.UseSqlServer(
                        context.Configuration.GetConnectionString("Online_Ticket_Booking_SystemDbContextConnection")));

                services.AddDefaultIdentity<ApplicationUser>(options =>
                {
                    options.SignIn.RequireConfirmedAccount = false;
                    options.Password.RequireLowercase = true;
                    options.Password.RequireUppercase = true;
                })
                   .AddEntityFrameworkStores<AuthDbContext>();
            });
        }
    }
}